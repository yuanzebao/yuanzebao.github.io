function getCurrentPage() {
  const path = window.location.pathname;
  let page = path.split('/').pop().replace('.html', '').toLowerCase();
  page = page.replace(/-([a-z])/g, (match, letter) => letter.toUpperCase());
  return page || 'home';
}

function translatePage() {
  const page = getCurrentPage();
  const pageTranslations = translations[page] || translations.home;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (pageTranslations[key]) {
      el.innerHTML = pageTranslations[key];
    }
  });

  const title = pageTranslations.title || '';
  const description = pageTranslations.description || '';
  
  if (title) document.title = title;
  if (description) {
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc) metaDesc.setAttribute('content', description);
  }
}

function initCookieConsent(immediate = false) {
  const cookieConsent = document.getElementById('cookieConsent');
  const acceptBtn = document.querySelector('.cookie-accept');
  const rejectBtn = document.querySelector('.cookie-reject');
  const hasConsent = localStorage.getItem('cookieConsent');

  if (!hasConsent && cookieConsent) {
    if (immediate) {
      cookieConsent.classList.add('show');
    } else {
      setTimeout(() => {
        cookieConsent.classList.add('show');
      }, 1000);
    }
  }

  if (acceptBtn) {
    acceptBtn.addEventListener('click', () => {
      localStorage.setItem('cookieConsent', 'accepted');
      if (cookieConsent) {
        cookieConsent.classList.remove('show');
      }
    });
  }

  if (rejectBtn) {
    rejectBtn.addEventListener('click', () => {
      localStorage.setItem('cookieConsent', 'rejected');
      if (cookieConsent) {
        cookieConsent.classList.remove('show');
      }
    });
  }
}

