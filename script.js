function getCurrentPage() {
  const path = window.location.pathname;
  let page = path.split('/').pop().replace('.html', '').toLowerCase();
  page = page.replace(/-([a-z])/g, (match, letter) => letter.toUpperCase());
  return page || 'home';
}

function getCurrentLang() {
  return localStorage.getItem('lang') || 'zh';
}

function setLang(lang) {
  localStorage.setItem('lang', lang);
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  translatePage();
}

function translatePage() {
  const page = getCurrentPage();
  const lang = getCurrentLang();
  const pageTranslations = translations[page] || translations.home;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (pageTranslations[key]) {
      el.innerHTML = pageTranslations[key][lang];
    }
  });

  const title = pageTranslations.title ? pageTranslations.title[lang] : '';
  const description = pageTranslations.description ? pageTranslations.description[lang] : '';
  
  if (title) document.title = title;
  if (description) {
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc) metaDesc.setAttribute('content', description);
  }
}

function initLanguageSelection() {
  const languageOverlay = document.getElementById('languageOverlay');
  const langZhBtn = document.querySelector('.lang-select-btn.lang-zh');
  const langEnBtn = document.querySelector('.lang-select-btn.lang-en');
  const hasLanguage = localStorage.getItem('lang');

  if (hasLanguage && languageOverlay) {
    languageOverlay.style.display = 'none';
  }

  if (langZhBtn && languageOverlay) {
    langZhBtn.addEventListener('click', function() {
      localStorage.setItem('lang', 'zh');
      languageOverlay.style.display = 'none';
      translatePage();
      initCookieConsent(true);
    });
  }

  if (langEnBtn && languageOverlay) {
    langEnBtn.addEventListener('click', function() {
      localStorage.setItem('lang', 'en');
      languageOverlay.style.display = 'none';
      translatePage();
      initCookieConsent(true);
    });
  }
}

function initCookieConsent(immediate = false) {
  const cookieConsent = document.getElementById('cookieConsent');
  const acceptBtn = document.querySelector('.cookie-accept');
  const rejectBtn = document.querySelector('.cookie-reject');
  const hasConsent = localStorage.getItem('cookieConsent');

  if (!hasConsent && cookieConsent) {
    if (immediate) {
      cookieConsent.classList.add('show');
    } else {
      setTimeout(() => {
        cookieConsent.classList.add('show');
      }, 1000);
    }
  }

  if (acceptBtn) {
    acceptBtn.addEventListener('click', () => {
      localStorage.setItem('cookieConsent', 'accepted');
      if (cookieConsent) {
        cookieConsent.classList.remove('show');
      }
    });
  }

  if (rejectBtn) {
    rejectBtn.addEventListener('click', () => {
      localStorage.setItem('cookieConsent', 'rejected');
      if (cookieConsent) {
        cookieConsent.classList.remove('show');
      }
    });
  }
}

document.addEventListener('DOMContentLoaded', () => {
  const currentLang = getCurrentLang();
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
    btn.addEventListener('click', () => setLang(btn.dataset.lang));
  });
  
  if (localStorage.getItem('lang')) {
    translatePage();
    initCookieConsent();
  }
  
  initLanguageSelection();
  initCarousel();
});

function initCarousel() {
  const track = document.getElementById('carouselTrack');
  const prevBtn = document.getElementById('carouselPrev');
  const nextBtn = document.getElementById('carouselNext');
  const indicators = document.getElementById('carouselIndicators');
  
  if (!track || !prevBtn || !nextBtn) return;
  
  const items = track.querySelectorAll('.carousel-item');
  const totalItems = items.length;
  let currentIndex = 0;
  
  function updateCarousel() {
    items.forEach((item, index) => {
      item.classList.remove('center', 'left', 'right', 'far-left', 'far-right', 'hidden');
      
      const relativeIndex = (index - currentIndex + totalItems) % totalItems;
      
      if (relativeIndex === 0) {
        item.classList.add('center');
      } else if (relativeIndex === 1) {
        item.classList.add('right');
      } else if (relativeIndex === totalItems - 1) {
        item.classList.add('left');
      } else if (relativeIndex === 2) {
        item.classList.add('far-right');
      } else if (relativeIndex === totalItems - 2) {
        item.classList.add('far-left');
      } else {
        item.classList.add('hidden');
      }
    });
    
    updateIndicators();
  }
  
  function updateIndicators() {
    if (indicators) {
      indicators.querySelectorAll('.indicator').forEach((indicator, index) => {
        indicator.classList.toggle('active', index === currentIndex);
      });
    }
  }
  
  function nextSlide() {
    currentIndex = (currentIndex + 1) % totalItems;
    updateCarousel();
  }
  
  function prevSlide() {
    currentIndex = (currentIndex - 1 + totalItems) % totalItems;
    updateCarousel();
  }
  
  prevBtn.addEventListener('click', prevSlide);
  nextBtn.addEventListener('click', nextSlide);
  
  if (indicators) {
    indicators.querySelectorAll('.indicator').forEach((indicator, index) => {
      indicator.addEventListener('click', () => {
        currentIndex = index;
        updateCarousel();
      });
    });
  }
  
  updateCarousel();
}